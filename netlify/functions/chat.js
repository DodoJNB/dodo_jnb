const RATE_LIMIT_WINDOW_MS = 60000;
const RATE_LIMIT_MAX_REQUESTS = 8;
const rateLimitMap = new Map();

function isRateLimited(ip) {
  const now = Date.now();
  const entry = rateLimitMap.get(ip);

  if (!entry || now - entry.windowStart > RATE_LIMIT_WINDOW_MS) {
    rateLimitMap.set(ip, { windowStart: now, count: 1 });
    return false;
  }

  entry.count += 1;
  if (entry.count > RATE_LIMIT_MAX_REQUESTS) return true;
  return false;
}

function cleanupRateLimitMap() {
  const now = Date.now();
  for (const [ip, entry] of rateLimitMap) {
    if (now - entry.windowStart > RATE_LIMIT_WINDOW_MS * 5) rateLimitMap.delete(ip);
  }
}

async function callCloudflareAI(url, options, maxRetries = 2) {
  let lastStatus = null;
  for (let attempt = 0; attempt <= maxRetries; attempt++) {
    const res = await fetch(url, options);
    if (res.ok) return res;

    lastStatus = res.status;
    if (res.status !== 429) return res;

    if (attempt < maxRetries) {
      const delayMs = 500 * Math.pow(2, attempt);
      await new Promise(r => setTimeout(r, delayMs));
    }
  }
  console.error('CF AI: limit zapytaЕ„ nadal przekroczony po retry, ostatni status:', lastStatus);
  return { ok: false, status: 429 };
}

